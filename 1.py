# 数据类型和变量

# 整型
a = 12345
print(a)

# 浮点型
b = 3.1415926
print(b)
print(b + 1.0)

# 字符串
c = "yuhaomiao"
print(c)

c1 = 'I\'m "OK"!'
print(c1)

# 转义字符\可以转义很多字符，比如\n表示换行，\t表示制表符，字符\本身也要转义，所以\\表示的字符就是\。
# r'' 表示 '' 内部的字符不转义。
print("line 1 \nline 2")
print("line 1 \n\tline 2")
print(r"line 1 \n\tline 2")

# 布尔型
d = True
print(d)
print(3 > 1)

# 空值
e = None
print(e)

